#is
#is not

a = 3
b = 6

hasil = a > b
print (hasil)
print (a, '>' , b, '=' ,hasil)

hasil = a < b
print (hasil)
print (a, '<' , b, '=' ,hasil)

hasil = a <= b
print (hasil)
print (a, '<=' , b, '=' ,hasil)

hasil = a == b
print (hasil)
print (a, '==' , b, '=' ,hasil)

hasil = a != b
print (hasil)
print (a, '!=' , b, '=' ,hasil)

#is
hasil = a is 3
print (hasil)
print (a, 'is' , 3, '=' ,hasil)

#is not
hasil = a is not 4
print (hasil)
print (a, 'is not' , 4, '=' ,hasil)









