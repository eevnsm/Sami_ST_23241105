a = 3
b = 6
#lebih dari

hasil = a > b
print (a, '>', b, '=', hasil)

#kurang dari
hasil = a < b
print (a, '>', b, '=', hasil)

# lebih dari sama dengan
hasil = a >= b
print (a, '>=', b, '=', hasil)

#kurang dari sama dengan
hasil = a <= b
print (a, '<=', b, '=', hasil)

#is

hasil = a is 2
print (a, 'is', 2, '=', hasil)

#is not
hasil = a is not 7
print (a, 'is not', 7, '=', hasil)